
export function add(number){
    let result = 0;

    if (result === 0) {

        result = number;
    }else{
        result += number;

    }
}

export function subtraction(number){
    let result = 0;

    if (result === 0) {

        result = number;
    }else{

        result -= number;
    }
}

export function multiply(number){
    let result = 0;

    if (result === 0) {

        result = number;
    }else{

        result *= number;
    }
}

export function division(number){
    let result = 0;

    if (result === 0) {

        result = number;
    }else{

        result /= number;
    }
}